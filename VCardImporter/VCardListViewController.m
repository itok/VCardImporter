#pragma mark - View lifecycle

- (void)viewDidLoad
{
    [super viewDidLoad];

    NSString* path = [NSSearchPathForDirectoriesInDomains(NSDocumentDirectory, NSUserDomainMask, YES) lastObject];
    NSFileManager* mgr = [[NSFileManager alloc] init];
    for (NSString* file in [mgr subpathsAtPath:path]) {
        if ([[file pathExtension] caseInsensitiveCompare:@"vcf"] == NSOrderedSame) {
            [list addObject:[path stringByAppendingPathComponent:file]];
        }
    }
}

